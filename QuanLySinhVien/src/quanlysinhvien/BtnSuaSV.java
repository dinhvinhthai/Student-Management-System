                        + hoten + "',Gioitinh='" + gioitinh + "',Ngaysinh='" 
                        + ngaysinh + "',Quequan='" + quequan + "', Nganh='" 
                        + nganh + "', Khoa=" + khoa + "WHERE MSSV='" + mssv + "'";
 
            	s.execute(up);
                
