    public BtnSuaSV(Object a){
        initComponents();
        String mssv = null, hotensv = null, gioitinh = null, quequan = null, nganh = null;
        int khoa = 0;
        Date ngaysinh= null;
        try{
        Statement s = clsConnection.con.createStatement();
        ResultSet rs = s.executeQuery("SELECT * FROM SinhVien WHERE MSSV = '"+a+"'");
        while(rs.next()){
            	mssv = rs.getString(1);
            	hotensv = rs.getString(2);
                gioitinh = rs.getString(3);
                ngaysinh = rs.getDate(4);
            	quequan = rs.getString(5);
                nganh = rs.getString(6);
            	khoa = rs.getInt(7);
        	}
        txtMSSV.setText(mssv);
        txtHoten.setText(hotensv);
        if(gioitinh.equals("Nam")) radNam.setSelected(true); 
        else radNu.setSelected(true);               
        txtNgaysinh.setText(ngaysinh.toString());
        txtQuequan.setText(quequan);
        txtNganh.setText(nganh);
        txtKhoa.setText( Integer.toString(khoa) );
        
        }
         catch(Exception ex)
        {
            ex.printStackTrace();
        }

    }

